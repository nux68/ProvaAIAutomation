using System;

namespace ProvaAIAutomation
{
    /// <summary>
    /// Classe per il calcolo della moltiplicazione.
    /// </summary>
    public static class Moltiplicazione
    {
        /// <summary>
        /// Restituisce il prodotto di due numeri interi.
        /// </summary>
        /// <param name="a">Primo fattore.</param>
        /// <param name="b">Secondo fattore.</param>
        /// <returns>Il prodotto a * b.</returns>
        public static int Calcola(int a, int b)
        {
            return a * b;
        }
    }
}
