using System;

namespace ProvaAIAutomation
{
    /// <summary>
    /// Helper per operazioni matematiche di base.
    /// </summary>
    public static class MathHelper
    {
        /// <summary>
        /// Calcola la differenza tra due numeri interi.
        /// </summary>
        /// <param name="a">Minuendo.</param>
        /// <param name="b">Sottraendo.</param>
        /// <returns>La differenza a - b.</returns>
        public static int Differenza(int a, int b)
        {
            return a - b;
        }
    }
}
