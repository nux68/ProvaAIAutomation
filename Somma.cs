using System;

namespace ProvaAIAutomation
{
    /// <summary>
    /// Classe che fornisce il metodo per calcolare la somma di due numeri.
    /// </summary>
    public static class Somma
    {
        /// <summary>
        /// Restituisce la somma di due numeri interi.
        /// </summary>
        /// <param name="a">Il primo numero intero.</param>
        /// <param name="b">Il secondo numero intero.</param>
        /// <returns>La somma a + b.</returns>
        public static int Calcola(int a, int b)
        {
            return a + b;
        }
    }
}
