using System;

namespace ProvaAIAutomation
{
    /// <summary>
    /// Classe che fornisce il metodo per calcolare la differenza tra due numeri.
    /// </summary>
    public static class Differenza
    {
        /// <summary>
        /// Restituisce la differenza tra due numeri interi.
        /// </summary>
        /// <param name="a">Il primo numero intero (minuendo).</param>
        /// <param name="b">Il secondo numero intero (sottraendo).</param>
        /// <returns>La differenza a - b.</returns>
        public static int Calcola(int a, int b)
        {
            return a - b;
        }
    }
}
