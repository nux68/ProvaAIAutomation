using System;

namespace ProvaAIAutomation
{
    /// <summary>
    /// Classe che fornisce operazioni matematiche di base.
    /// </summary>
    public static class Operazioni
    {
        /// <summary>
        /// Restituisce la somma di due numeri interi.
        /// </summary>
        /// <param name="a">Il primo numero intero.</param>
        /// <param name="b">Il secondo numero intero.</param>
        /// <returns>La somma di a e b.</returns>
        public static int Somma(int a, int b)
        {
            return a + b;
        }
    }
}
